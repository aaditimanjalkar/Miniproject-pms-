    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String JTitle=jTextField1.getText();
        String Package=jTextField2.getText();
        String cgpa=jTextField3.getText();
        String kt =jTextField4.getText();
        String cin=jTextField5.getText();
        String CName=jTextField6.getText();
        
        try
        {
          Connection con=DBConnectionProvider.getCon();
          PreparedStatement ps=con.prepareStatement("insert into jobs values (?,?,?,?,?,?)");
          ps.setString(1, JTitle);
          ps.setString(2, Package);
          ps.setString(3, cgpa);
          ps.setString(4, kt);
          ps.setString(5, cin);
          ps.setString(6, CName);
          
          ps.executeUpdate();
          
          JOptionPane.showMessageDialog(null, "Successfully Added");
          setVisible(false);
          new CompanyRegistration().setVisible(true);
        }
        catch(Exception e)
        {
          JOptionPane.showMessageDialog(null, e);
          
        }
    }//GEN-LAST:event_jButton1ActionPerformed

